// 변수의 기본 자료형

// Number
var a=10, b=3.14;

// String
var c="안녕하세요", d="a";

// boolean
var e=true, f=false;


// typeof() 명령어
// 괄호 안의 변수의 자료형을 알려준다.

console.log(a,"의 타입은",typeof(a));
console.log(c,"의 타입은",typeof(c));
console.log(e,"의 타입은",typeof(e));

// 변수에는 기본 자료형 뿐만 아니라 object 와 function도 할당할 수 있다.